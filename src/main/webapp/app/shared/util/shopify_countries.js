export const getShopifyCountries = () => {
  let finalCountries = [];
  if (shopify_countries_List) {
    Object.keys(shopify_countries_List).forEach((countryKey, countryIndex) => {
      const countryTemp = shopify_countries_List[countryKey];
      let countryFinal = {
        name: countryKey, code: countryTemp.code, provinces: []
      };
      if (countryTemp.provinces) {
        const provincesTemp = countryTemp.provinces;
        Object.keys(provincesTemp).forEach((provinceKey, provinceIndex) => {
          const province = provincesTemp[provinceKey];
          const provinceLocal = {
            name: provinceKey, code: province.code
          };
          countryFinal.provinces.push(provinceLocal);
        })
      }
      finalCountries.push(countryFinal);
    });
  }
  return finalCountries;
}
